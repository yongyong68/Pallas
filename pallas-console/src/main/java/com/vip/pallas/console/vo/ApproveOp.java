package com.vip.pallas.console.vo;

import com.vip.pallas.console.vo.base.BaseApproveOp;

import java.io.Serializable;

public class ApproveOp extends BaseApproveOp implements Serializable {
    private Long id;
    private String ids;
    private String state;
    private String note;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getIds() {
        return ids;
    }

    public void setIds(String ids) {
        this.ids = ids;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}